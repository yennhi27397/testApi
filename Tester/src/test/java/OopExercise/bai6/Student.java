package OopExercise.bai6;

public class Student  {
  public String name;
  public int age;
  public String homeTown;

  public Student(String name, int age, String homeTown) {
    this.name = name;
    this.age = age;
    this.homeTown = homeTown;
  }

  public String getName() {
    return name;
  }

  public int getAge() {
    return age;
  }

  public String getHomeTown() {
    return homeTown;
  }
}



