package OOP1;

public class Parent1 {
  private String name;
  private String character;
  private int age;
  private String skinColor;

  public Parent1(String name, String character, int age, String skinColor) {
    this.name = name;
    this.character = character;
    this.age = age;
    this.skinColor = skinColor;
  }

  public String getName() {
    return this.name;
  }

}
